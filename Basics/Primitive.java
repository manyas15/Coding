public class Primitive {
    public static void main(String[] args) {
        int rollno = 64;
        char name = 'm';
        float marks = 345.67f;
        double largeDecimalValue = 232434542.3424;
        long largeInteger = 234324455L;
        boolean check = true;
        System.out.println("Roll no: " + rollno);
        System.out.println("Name(initial): " + name);
        System.out.println("Marks: " + marks);
        System.out.println("Large Decimal : " + largeDecimalValue);
        System.out.println("Large integer : " + largeInteger);
        System.out.println("Boolean: " + check);
    }
}